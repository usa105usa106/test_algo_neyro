APP_VERSION = "64_full_GMAIL_PORT80_PROBE_DUAL_PERSISTENCE"


def _normalize_public_base_url(value: str) -> str:
    """Return one external HTTPS base URL without callback/health paths.

    Coolify may expose its wildcard URL to the container with an ``http``
    scheme even though users reach the reverse proxy over HTTPS. Google OAuth
    requires HTTPS for ``gmail.send``, so non-local hosts are always normalized
    to HTTPS. Port 80/443 and known endpoint suffixes are removed.
    """
    from urllib.parse import urlsplit, urlunsplit

    value = (value or "").strip().strip('"\'')
    if not value:
        return ""
    value = value.split(",", 1)[0].strip()
    if "://" not in value:
        value = f"https://{value}"

    parsed = urlsplit(value)
    host = (parsed.hostname or "").lower()
    if not host:
        return ""
    local_hosts = {"localhost", "127.0.0.1", "::1"}
    scheme = parsed.scheme.lower() or "https"
    if host not in local_hosts:
        scheme = "https"

    port = parsed.port
    if port in {80, 443}:
        port = None
    netloc = host if port is None else f"{host}:{port}"

    path = parsed.path.rstrip("/")
    for suffix in ("/gmail/callback", "/healthz"):
        if path.endswith(suffix):
            path = path[: -len(suffix)].rstrip("/")
            break
    return urlunsplit((scheme, netloc, path, "", "")).rstrip("/")


def _resolve_gmail_public_base_url() -> str:
    """Resolve the public URL generated by Coolify.

    v64 deliberately listens on container port 80. This avoids custom-port
    proxy routing entirely: Coolify's standard ``SERVICE_URL_<ID>`` route can
    reach the callback server without a manual Domains field or ``:8080``.
    """
    for key in (
        "GMAIL_PUBLIC_BASE_URL",
        "GMAIL_REDIRECT_URI",  # backward compatibility; endpoint is stripped
        "SERVICE_URL_GMAIL-AUTH",
        "SERVICE_URL_GMAIL_AUTH",
        # Backward compatibility with v61/v63 generated variables.
        "SERVICE_URL_GMAIL-AUTH_8080",
        "SERVICE_URL_GMAIL_AUTH_8080",
        "SERVICE_FQDN_GMAIL-AUTH",
        "SERVICE_FQDN_GMAIL_AUTH",
        "COOLIFY_URL",
    ):
        base = _normalize_public_base_url(os.getenv(key, ""))
        if base:
            return base
    return ""


def _endpoint(base: str, path: str) -> str:
    return f"{base.rstrip('/')}/{path.lstrip('/')}" if base else ""


# Backward-compatible helpers retained for older tests and local tooling.
def _normalize_callback_url(value: str) -> str:
    return _endpoint(_normalize_public_base_url(value), "/gmail/callback")


def _resolve_gmail_redirect_uri() -> str:
    return _endpoint(_resolve_gmail_public_base_url(), "/gmail/callback")


